_INVOICE_DIR = Path("./storage/sales/invoices")
_INVOICE_DIR.mkdir(parents=True, exist_ok=True)


def _cred(key: str) -> Optional[str]:
    try:
        from backend.services.credentials_vault import credentials_vault
        return credentials_vault.get(key) or None
    except Exception:
        return None


def _next_invoice_number() -> str:
    year = datetime.utcnow().year
    row = business_db.query_one(
        "SELECT number FROM invoices WHERE number LIKE ? ORDER BY id DESC LIMIT 1",
        (f"INV-{year}-%",),
    )
    if not row:
        return f"INV-{year}-0001"
    try:
        last_n = int(row["number"].rsplit("-", 1)[-1])
        return f"INV-{year}-{last_n + 1:04d}"
    except Exception:
        return f"INV-{year}-{uuid.uuid4().hex[:6].upper()}"


